// ÏÇ¨Ïö©Ïûê Î©îÏãúÏßÄÏóêÏÑú Ï≤®Î∂ÄÌååÏùº Ï∂îÏ∂ú (Ï≤´Î≤àÏß∏ content Ïù¥ÌõÑÏùò Î™®Îì† Í≤É)
const getUserAttachments = (message: any): any[] => {
  if (!message.content || !Array.isArray(message.content)) return []
  
  // Ï≤´ Î≤àÏß∏ contentÎ•º Ï†úÏô∏Ìïú ÎÇòÎ®∏ÏßÄ Î™®Îì† contentÎ•º Ï≤®Î∂ÄÌååÏùºÎ°ú Ï≤òÎ¶¨
  // (Îëê Î≤àÏß∏ textÎèÑ Ï≤®Î∂ÄÌååÏùºÎ°ú Ï∑®Í∏â)
  return message.content.slice(1).map((c: any, index: number) => {
    // text ÌÉÄÏûÖÎèÑ Ï≤®Î∂ÄÌååÏùºÎ°ú Ï≤òÎ¶¨ (ÏõåÌÅ¨ÌîåÎ°úÏö∞ JSON Îì±)
    if (c.type === 'text') {
      // JSON ÌååÏã± ÏãúÎèÑ
      let isJson = false
      let jsonData = null
      try {
        jsonData = JSON.parse(c.text)
        isJson = true
      } catch {}
      
      // ÏõåÌÅ¨ÌîåÎ°úÏö∞ JSONÏóêÏÑú Ïù¥Î¶Ñ Ï∂îÏ∂ú
      let fileName = `attachment_${index + 1}.${isJson ? 'json' : 'txt'}`
      if (isJson && jsonData) {
        // ÏõåÌÅ¨ÌîåÎ°úÏö∞ Ïù¥Î¶Ñ Ï∂îÏ∂ú ÏãúÎèÑ
        if (jsonData.workflow_name) {
          fileName = `${jsonData.workflow_name}.json`
        } else if (jsonData.workflow_json?.name) {
          fileName = `${jsonData.workflow_json.name}.json`
        } else if (jsonData.name) {
          fileName = `${jsonData.name}.json`
        }
      }
      
      return {
        ...c,
        type: isJson ? 'json' : 'text',
        name: fileName,
        text: c.text
      }
    }
    return c
  })
}

