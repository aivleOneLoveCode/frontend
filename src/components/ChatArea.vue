const isInitialRender = ref(true)

// ÏûêÎèô Î¶¨ÏÇ¨Ïù¥Ïßï Í∏∞Îä• (HTMLÍ≥º ÎèôÏùº)
const autoResizeTextarea = () => {
  nextTick(() => {
    const textarea = textareaRef.value
    if (textarea) {
      textarea.style.height = 'auto'
      const newHeight = Math.min(textarea.scrollHeight, 400)
      textarea.style.height = newHeight + 'px'
      
      // ÏµúÎåÄ ÎÜíÏù¥Ïóê ÎèÑÎã¨ÌñàÏùÑ Îïå Ïä§ÌÅ¨Î°§ ÌëúÏãú
      if (textarea.scrollHeight > 400) {
        textarea.style.overflowY = 'auto'
      } else {
        textarea.style.overflowY = 'hidden'
      }
    }
  })
}

// contenteditable input Ìï∏Îì§Îü¨
const handleInput = (event: Event) => {
  const target = event.target as HTMLDivElement
  const text = target.textContent || ''
  emit('update:input-text', text)
  autoResizeTextarea()
}

// Î©îÏãúÏßÄ Ï†ÑÏÜ° Ìï∏Îì§Îü¨
const handleSendMessage = () => {
  emit('send-message')
  // Î©îÏãúÏßÄ Ï†ÑÏÜ° Ïãú Í∞ïÏ†úÎ°ú Îß® ÏïÑÎûò Ïä§ÌÅ¨Î°§
