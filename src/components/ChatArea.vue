}

// inputText Î≥ÄÍ≤Ω Í∞êÏßÄ
watch(() => props.inputText, (newValue) => {
  // contenteditable divÏùò ÎÇ¥Ïö© ÏóÖÎç∞Ïù¥Ìä∏
  if (textareaRef.value && textareaRef.value.textContent !== newValue) {
    textareaRef.value.textContent = newValue
