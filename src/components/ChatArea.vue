  autoResizeTextarea()
})

// Ïä§ÌÅ¨Î°§Ïù¥ Îß® ÏïÑÎûòÏóê ÏûàÎäîÏßÄ ÌôïÏù∏
const isScrolledToBottom = (): boolean => {
  if (!messagesContainer.value) return true
  const container = messagesContainer.value
  const threshold = 100 // 100px Ï†ïÎèÑ Ïó¨Ïú†Î•º Îë†
  return container.scrollHeight - container.scrollTop - container.clientHeight <= threshold
}

// Ïä§ÌÅ¨Î°§ Ïù¥Î≤§Ìä∏ Ìï∏Îì§Îü¨ (Ï¶âÍ∞Å Î∞òÏùë + Ïä§Î°úÌãÄÎßÅ)
const handleScroll = () => {
  // Ï¶âÏãú ÏÇ¨Ïö©Ïûê Ïä§ÌÅ¨Î°§ ÏÉÅÌÉú ÏóÖÎç∞Ïù¥Ìä∏ (Ïã§ÏãúÍ∞Ñ Î∞òÏùë)
  const isAtBottom = isScrolledToBottom()
  isUserScrolling.value = !isAtBottom
  
  // Ïä§Î°úÌãÄÎßÅÏúºÎ°ú ÏÑ±Îä• ÏµúÏ†ÅÌôî (ÏÉÅÌÉú ÏïàÏ†ïÌôî)
  if (scrollThrottleTimeout.value) {
    clearTimeout(scrollThrottleTimeout.value)
  }
  
  scrollThrottleTimeout.value = window.setTimeout(() => {
    isUserScrolling.value = !isScrolledToBottom()
    scrollThrottleTimeout.value = null
  }, 50) // 150ms ‚Üí 50msÎ°ú Ï§ÑÏó¨ÏÑú Îçî Îπ†Î•∏ Î∞òÏùë
}

// Î©îÏãúÏßÄ ÏòÅÏó≠ÏùÑ Îß® ÏïÑÎûòÎ°ú Ïä§ÌÅ¨Î°§ (Ïä§ÎßàÌä∏ Ïä§ÌÅ¨Î°§)
const scrollToBottom = (immediate = false) => {
  nextTick(() => {
    if (messagesContainer.value && !isUserScrolling.value) {
      messagesContainer.value.scrollTo({
        top: messagesContainer.value.scrollHeight,
        behavior: immediate ? 'instant' : 'smooth'
      })
    }
  })
}

// Ï¶âÏãú Îß® ÏïÑÎûòÎ°ú Ïä§ÌÅ¨Î°§ (Ïï†ÎãàÎ©îÏù¥ÏÖò ÏóÜÏùå)
const scrollToBottomInstant = () => {
  // DOM Î†åÎçîÎßÅÏù¥ ÏôÑÏ†ÑÌûà ÎÅùÎÇ† ÎïåÍπåÏßÄ Í∏∞Îã§Î¶∞ ÌõÑ Ïä§ÌÅ¨Î°§
