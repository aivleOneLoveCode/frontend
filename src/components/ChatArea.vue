  // Ìè¨Ïª§Ïä§Îßå Ï≤òÎ¶¨ (Ï¥àÍ∏∞ÌôîÎäî Î∂ÄÎ™®ÏóêÏÑú Ï≤òÎ¶¨)
  nextTick(() => {
    if (textareaRef.value) {
      textareaRef.value.focus()
      autoResizeTextarea()
    }
  })
}

// Î©îÏãúÏßÄ Ï§ëÎã® Ìï∏Îì§Îü¨
const handleStopMessage = () => {
